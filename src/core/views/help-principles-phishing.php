<p>Podobně jako při psaní běžného e-mailu je nutné i&nbsp;při <strong>vytváření cvičného podvodného e-mailu</strong> pamatovat na určité <strong>zásady</strong>. Cílem je vždy sestavit cvičný podvodný e-mail, který uživatele <strong>naučí odhalovat typické znaky</strong> a&nbsp;techniky používané ve <strong>phishingových e-mailech</strong>.</p>

<h4 class="pb-2 mb-3 border-bottom">Znaky phishingu</h4>
<p>Každý cvičný podvodný e-mail by měl především obsahovat <strong>charakteristické znaky phishingu</strong>, které lze nalézt i&nbsp;v&nbsp;<strong>reálném phishingovém e-mailu</strong>. Je vhodné, aby se ve cvičném phishingovém e-mailu vyskytly <strong>alespoň tři indicie</strong>, na základě kterých bylo možné <strong>podvod rozpoznat</strong>, typicky:</p>
<ul>
  <li><strong>podvržená adresa</strong> odesílatele,</li>
  <li><strong>odkaz na podvodnou stránku,</strong></li>
  <li>podle <strong>obsahu</strong> v e-mailu:
    <ul>
      <li><strong>použitý jazyk</strong> (strojová čeština, neočekávaně anglicky psaný e-mail o&nbsp;mzdovém lístku),</li>
      <li><strong>relevantnost</strong> sdělení (obsah, který by měl být určen někomu jinému),</li>
      <li><strong>časový nátlak</strong> na uživatele (nutnost obratem reagovat &ndash; kliknout na podvodný odkaz),</li>
      <li><strong>hrozba ztráty, příležitosti</strong> (o&nbsp;co uživatel přijde &ndash; např. zablokování účtu, odměny zdarma),</li>
      <li><strong>vydávání se za autoritou</strong> (motivace na zprávu reagovat &ndash; např. vydávání se za administrátora serveru).</li>
    </ul>
  </li>
</ul>

<div class="alert alert-info" role="alert">
  Ve cvičném phishingu by <strong>nemělo být použito</strong> (ani překrouceno) <strong>jméno existující osoby</strong> nebo <strong>jméno existujícího oddělení</strong> v&nbsp;organizaci. Osoba nebo oddělení by mohla být následně <strong>mylně nařčena</strong> nebo zaplavena nevyžádanými dotazy.
</div>

<h4 class="pb-2 mb-3 border-bottom">Obtížnost</h4>
<p>Cvičný podvodný e-mail by měl být <strong>přizpůsoben zkušenostem příjemců</strong> &ndash; <strong>začátečníkům</strong> a&nbsp;neproškoleným je vhodné rozesílat lehčí phishing (s&nbsp;více indiciemi), <strong>pokročilým uživatelům</strong> je možné rozesílat složitější a&nbsp;cílenější <strong>spear phishing</strong>. V&nbsp;každém cvičném podvodném e-mailu by se však měly vyskytnout <strong>jasné indicie</strong>, na základě kterých bylo možné phishing rozpoznat.</p>

<h4 class="pb-2 mb-3 border-bottom">Interval rozesílání</h4>
<p>Vhodné je cvičný phishing rozesílat <strong>3&times; až 4&times;&nbsp;ročně</strong>. Uživatelé tak o&nbsp;<strong>hrozbě phishingu</strong> (a&nbsp;jeho nových technikách) budou informováni, ale zároveň je nebudou podobná cvičení <strong>obtěžovat</strong>.</p>

<h4 class="pb-2 mb-3 border-bottom">Podvodné stránky</h4>
<p>Podvodné stránky se mohou <strong>vizuálně shodovat</strong> se skutečnou, pravou webovou stránku (např. s&nbsp;<strong>přihlašovací stránkou</strong> organizace), a&nbsp;to včetně důvěryhodného HTTPS certifikátu.</p>

<div class="alert alert-info" role="alert">
  Při použití podvodné stránky běžící na <strong>nezabezpečeném protokolu HTTP</strong> je možné, že data zadaná na podvodné stránce mohou být <strong>odposlechnuta</strong>. Uživatelům, kteří do takové podvodné stránky vyplnili platné přihlašovací údaje, by mělo být doporučeno <strong>provést změnu hesla</strong>.
</div>

<h4 class="pb-2 mb-3 border-bottom">Náměty</h4>
Mezi <strong>vhodná témata</strong> pro cvičné podvodné e-maily například patří:
<ul>
  <li><strong>různé notifikace</strong> &ndash; vypršení hesla, žádost o&nbsp;sdílení dokumentu, kontrola použití účtu z&nbsp;jiné lokality apod.,</li>
  <li><strong>kontrola nebo potvrzení</strong> údajů,</li>
  <li><strong>test nové aplikace</strong>,</li>
  <li><strong>elektronické odsouhlasení</strong> navýšení platu.</li>
</ul>